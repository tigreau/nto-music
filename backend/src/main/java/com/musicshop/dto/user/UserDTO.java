package com.musicshop.dto.user;

public record UserDTO(
        Long id,
        String firstName,
        String lastName,
        String email,
        String phoneNumber,
        String street,
        String number,
        String postalCode,
        String city,
        String country) {

    public Long getId() {
        return id;
    }

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public String getEmail() {
        return email;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public String getStreet() {
        return street;
    }

    public String getNumber() {
        return number;
    }

    public String getPostalCode() {
        return postalCode;
    }

    public String getCity() {
        return city;
    }

    public String getCountry() {
        return country;
    }
}
